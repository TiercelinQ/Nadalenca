<h1>Descaptar la nòtra istòria</h1>
<hr />


<p>« Nadalenca » trapa son origina dins lo mot Nadal (Noël en francès). Jos l’impulsion de Joan Roqueta, prèire e escriveire occitan (Joan Larzac),
 Uc Brunet a recampat qualques coristas amators per cantar de Nadalets (cants tradicionals de Nadal) e manténer viva la tradicion del Nadal occitan 
 d’en primièr dins de vilòtas del ròdol de Mont-Pelhièr ( Sant Bauzèli de Montmel, Salinèlas) e dempuèi 1990 a Mont-Pelhièr.</p>

<p>En 1999 es constituida l’associacion lei 1901 « Corala Nadalenca » còr que se fa veire l’annada tota en amplificant son repertòri a totes 
los cants polifonics de la tradicion occitana. Per çò faire, l’associacion a desvelopat un projecte de recèrca de cants en lenga d’òc, 
mantuns n’existissián que dins la tradicion orala, que tornèt faire escriure e/o armonisar. Uèi lo repertòri integra tanben d’òbras classicas adaptadas
 a l’occitan.</p>

<p>En 2003 la « Corala Nadalenca » foguèt convidada als primièrs rescontres internacionals de coralas de Tiana (Catalonha). Dempuèi lo còr participa 
regularament a d’escambis amb d’autras coralas que se traduison per de desplaçaments (en França, a l’estrangièr) e a la recepcion d’aquestas a Mont-Pelhièr.
 De ligams, particularament estreches, se son tessats amb un autre còr de l’aglomeracion de Barcelona : la « Joventud Tianenca » e balhan l’escasença a un
 escambi cada an.</p>

<p>Dempuèi 2005 lo còr occitan es membre de « l’Association des Chœurs de Montpellier » (ACM) que fedèra mai de 30 còrs e gerís
 la « Maison des Chœurs de Montpellier ».Aquesta associacion permet un rescontre e d’escambis demest las diferentas formacions 
montpelhierencas que se concretisan per de concèrts porgits ensems e de projectes d’animacion que recampan un potencial de mai de 1000 coristas.
Per decision de l’amassada generala de febrièr 2006, la « Corala Nadalenca » es venguda « Nadalenca » còr polifonic occitan de Mont-Pelhièr.</p>