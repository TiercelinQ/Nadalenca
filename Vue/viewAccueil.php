	<span id="accueil">
		<h3>« Nadalenca » est le chœur polyphonique Occitan de Montpellier</h3>
	</span>

	<p>Composé d’une trentaine de choristes placés , le choeur interprète des oeuvres polyphoniques tirées du répertoire Occitan : Nadalets et chants traditionnels, ainsi que des oeuvres classiques adaptées en Occitan.</p>

	<p>Nadalenca participe à la promotion du patrimoine culturel Occitan par les concerts donnés dans toute l’Occitanie et au-delà, ainsi que par des échanges réguliers avec d’autres chœurs en France et à l’étranger qui lui ont permis de s’ouvrir à d’autres langues romanes Méditerranéennes.</p>


