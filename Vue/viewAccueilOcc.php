	<span id="accueil">
		<h3>« Nadalenca » es lo còr polifonic occitan de Montpelhièr</h3>
	</span>
	
	<p>Compausat d’un trentenat de coristast, lo còr interpreta d’òbras polifonicas sortidas del repertòri occitan : Nadalets e cants tradicionals e 
	tanben d’òbras classicas adaptadas en occitan.</p>

	<p>Nadalenca participa a la promocion del patrimòni cultural occitan amb de concèrts balhats dins Occitània tota e enlà, e encara amb d’escambis 
	regulars amb d’autras lengas romanicas mediterranèas.<p>
	

