<h1>Noel Occitan</h1>
<hr />

<p>Depuis 1990, chaque 24 Décembre, Nadalenca organise le Noël Occitan de Montpellier (Veillée traditionnelle et Messe de Minuit en langue d’oc) qui est devenu une véritable. La veillée traditionnelle autour de la crèche permet d’attendre la Messe de Minuit et propose des contes, textes en Occitan, et bien sur les fameux « Nadalets » traditionnels. A Minuit, célébration de la Messe de Noël. Tout est en Occitan, les textes des chants, de l·homélie et de la messe sont traduits dans les livrets mis à disposition pour tous les participants.</p>

