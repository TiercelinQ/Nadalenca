<h1>Recrutement</h1>  
<hr />

<p>Le chœur polyphonique Occitan « Nadalenca » recrute des nouveaux choristes. 
L’admission dans le chœur peut se faire toute l’année, mais 2 périodes sont plus favorables : En 
septembre au moment de la reprise des répétitions sur le répertoire « Nadalets » et au mois 
de Janvier lors du démarrage du travail sur le répertoire « Traditionnel et Classique ».</p>

<h2>Modalités - Conditions</h2>
<hr />

<p>Nadalenca recrute des personnes ayant une pratique du chant choral ou montrant une réelle 
motivation pour l’apprentissage des partitions et l’acquisition d’une technique vocale. Il n’est 
cependant pas nécessaire de savoir déchiffrer la musique.</p>
<p>En ce qui concerne la langue Occitane : Chanter en occitan n’est pas plus difficile que de 
chanter en Allemand, en Latin ou en Italien,…. Il n’est donc pas nécessaire de maîtriser 
cette langue, par contre chanter à Nadalenca permet d’acquérir des rudiments de l’Occitan 
par un apprentissage plus ludique. Bien sur un intérêt pour la langue Occitane et la culture 
régionale renforcent le plaisir de chanter à « Nadalenca ».</p>
<p>La cotisation annuelle 2013 est fixée à 130€ , comprenant l’adhésion à l’association, les 
photocopies et supports CD audio de travail, et l’inscription aux cours de technique voc
Pour faire acte de candidature, vous pouvez remplir directement le formulaire en base de page.</p>


<h2>Répétitions</h2>
<hr />


<p>Répétition hebdomadaire (en général le Mercredi soir) à la Maison de l’Occitanie , 117 rue 
des Etats Généraux , 34000 Montpellier.</p>
<p>Cours de technique vocale assurés par un professeur de chant <a href="http://philippe.vialard.free.fr" target="_blank">Philippe VIALARD</a> un 
Mercredi sur deux de 20H00 à 21H00 juste avant la répétition.</p>
