		<h3>Los cants tradicionals</h3>
	<p>Los cants tradicionals son de cants populars amb d’autors mai o mens coneguts. Los 

tèmas s’inspiran mai que mai de la vida vidanta rurala marcada pel ritme de las sasons, lo 

trabalh, las fèstas, los eveniments de la vida, … Èran cantats al trabalh perlos lauraires, 

dalhaires, vendemiaires, menestrals per sosténer lo ritme de lor rude prètzach. Nos 

foguèron transmeses de generacion en generacion, directament de la boca dels aujòls dins 

las velhadas, vendémias, segas, repaisses de familha e festius.</p>

<p>Son originaris del Carcin, del País niçard, del Lengadòc : Jol pont de Mirabèl, La tanta 

Isabèla, Los esclòps, Magali, Copa santa, Se canta, Pichòt òme, Calant de Vilafranca, Lo 

cocut, Bèl Clapàs, L’immortèla, La caravana occitana, Triste es lo cèl, …</p>

<p>Las òbras classicas son adaptadas en occitan per Joan Larzac, l’interpretacion d’aquelas 

òbras es una causida artistica que permet d’amplificar e d’enriquir lo repertòri, de s’apropriar 

una musica de còps complexa, e de faire viure la lenga occitana dins un domeni inedit. Los 

compositors de predileccion son : Bach : Qu’es aquela granda clartat, Dins aquela nuòch, 

Coma un pan caud, Lo pan de Dieu. E. du Caurroy : Nadal. Pretorius : Dins una bauma 

escura, Uòi es nascut Emmanuel. Pau Casals : El cant dels ocells. Benedetto Marcello 

(1686-1739) Psaume XVIII : los cèls immensis, …</p>
	
	
<p>En completar d’annada sus annada son repertòri amb d’armonisacions novèlas, Nadalenca participa a l’enriquiment del patrimòni coral en occitan e tanben a sa difusion de cap als autres còrs e al public.</p>
