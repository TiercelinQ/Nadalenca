		<h1>Découvrez notre histoire</h1>
		<hr />
		
		<p>
		« NADALENCA » trouve son origine dans le mot « Nadal » qui signifie Noël. 
		Sous l’impulsion de Jean ROUQUETTE, prêtre et écrivain Occitan (Joan LARZAC), 
		Hugues BRUNET a rassemblé dès 1982 quelques choristes amateurs pour chanter des Nadalets (Chants traditionnels de Noël en Occitan) 
		et maintenir vivante la tradition du <a href="#" target="_blank">Noël Occitan</a>. 
		</p>
		
		<p>
		En 1999 est constituée l’association loi 1901 « CORALA NADALENCA », chœur qui se produira toute l’année en 
		élargissant son répertoire à tous les chants polyphoniques de la tradition occitane. Pour cela, l’association 
		a développé un projet de recherche de chants en langue d’Oc - certains n’existant que par la tradition orale - 
		qu’elle a fait écrire et/ou harmoniser. Aujourd’hui le répertoire intègre aussi des œuvres classiques adaptées à l’Occitan. 
		</p>
		
		<p>
		En 2003 la «CORALA  NADALENCA » a été invitée aux premières rencontres internationales de chorales de Tiana (Catalogne). 
		Depuis le chœur participe régulièrement à des échanges avec d’autres chorales qui se traduisent par des déplacements (en France, à l’étranger) 
		et à la réception de celles-ci à Montpellier. Des liens particulièrement étroits se sont tissés avec un chœur catalan de l’agglomération de Barcelone 
		: la « Joventut Tianenca » donnant lieu à un échange annuel.
		</p>

		<p>
		Depuis 2005, le chœur Occitan est membre de l'<a href="http://maisondeschoeurs.free.fr/association.htm" target="_blank">Association des Chœur de Montpellier</a> 
		(ACM) qui fédère plus de 30 chœurs et gère la Maison des Chœurs polyphoniques de la tradition occitane. 
		Pour cela, l’association a développé un projet de recherche de chants en langue d’Oc - 
		certains n’existant que par la tradition orale - qu’elle a fait écrire et/ou harmoniser. 
		Aujourd’hui le répertoire intègre aussi des œuvres classiques adaptées à l’Occitan. 
		</p>
		
		<p>
		Par décision de l’assemblée générale de Février 2006, la « Corala Nadalenca », est devenue « NADALENCA » Chœur polyphonique Occitan de Montpellier.
		</p>

	